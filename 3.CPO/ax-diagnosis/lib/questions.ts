/** Score a single question. Returns 0–100. */
export function scoreQuestion(q: Question, answer: number | number[]): number {
  if (q.type === "mc") {
    const idx = answer as number;
    if (q.isLikert) {
      // 1-indexed choice value (0-4 idx) → 0,25,50,75,100
      return idx * 25;
    }
    if (q.correctIndices) {
      // Knowledge question: correct or not
      return q.correctIndices[0] === idx ? 100 : 0;
    }
    // Scenario mc: 0-indexed → 0,25,50,75,100
    return idx * 25;
  } else {
    // cb
    const selected = answer as number[];
    if (!q.correctIndices || q.correctIndices.length === 0) return 50;
    const total = q.choices.length;
    const totalCorrect = q.correctIndices.length;
    const totalWrong = total - totalCorrect;
    const correctSelected = selected.filter((i) => q.correctIndices!.includes(i)).length;
    const wrongSelected = selected.filter((i) => !q.correctIndices!.includes(i)).length;
    const raw =
      correctSelected / totalCorrect -
      (totalWrong > 0 ? wrongSelected / totalWrong : 0);
    return Math.round(Math.max(0, raw) * 100);
  }
}

/** Compute per-axis scores (0–100) from a map of questionId → answer. */
export function computeAxisScores(
  questions: Question[],
  answers: Record<string, number | number[]>
): Record<Axis, number> {
  const axes: Axis[] = ["org_hard", "org_soft", "ind_hard", "ind_soft"];
  const result = {} as Record<Axis, number>;
  for (const axis of axes) {
    const qs = questions.filter((q) => q.axis === axis && answers[q.id] !== undefined);
    if (qs.length === 0) {
      result[axis] = 0;
      continue;
    }
    const total = qs.reduce((sum, q) => sum + scoreQuestion(q, answers[q.id]), 0);
    result[axis] = Math.round(total / qs.length);
  }
  return result;
}
